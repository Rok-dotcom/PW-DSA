package ArrayList;

import java.util.ArrayList;

public class ArrayListMethods {


    public static void main(String[] args){
        ArrayList<Integer> arr = new ArrayList<>();


        arr.add(1);     // adding integer
        arr.remove(0); // remove the element which is on this index
        arr.set(1,5);    // add element in random index.
        arr.get(1);        // get the element of the index.
        arr.isEmpty();
        arr.clear();
    }







}
